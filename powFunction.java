import java.util.Scanner;
public class   powFunction {
    static int add(int l) {
        return l*l;
    }


    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        int l;
        System.out.println("Enter the number");
        l= scan.nextInt();


        System.out.println(add(l));






    }
}
